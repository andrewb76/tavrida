#!/usr/bin/env bash
# Write docker/swarm/dev.env from environment (CI / local).
# Required: DEV_DOMAIN, ACME_EMAIL, GIT_SHA (or GITHUB_SHA)
set -euo pipefail

ROOT="$(cd "$(dirname "$0")/../.." && pwd)"
OUT="${1:-${ROOT}/docker/swarm/dev.env}"

GHCR_OWNER="${GHCR_OWNER:-${GITHUB_REPOSITORY_OWNER:-andrewb76}}"
GIT_SHA="${GIT_SHA:-}"
if [[ -z "$GIT_SHA" && -n "${GITHUB_SHA:-}" ]]; then
  GIT_SHA="${GITHUB_SHA:0:7}"
fi
GIT_SHA="${GIT_SHA:-latest}"

TAVRIDA_REPO_ROOT="${TAVRIDA_REPO_ROOT:-/opt/tavrida}"
DEV_DOMAIN="${DEV_DOMAIN:?DEV_DOMAIN is required}"
ACME_EMAIL="${ACME_EMAIL:?ACME_EMAIL is required}"

cat >"$OUT" <<EOF
# Generated by ci-write-dev-env.sh — do not commit
DEV_DOMAIN=${DEV_DOMAIN}
ACME_EMAIL=${ACME_EMAIL}
TRAEFIK_SWARM_NETWORK=${TRAEFIK_SWARM_NETWORK:-tavrida-dev_tavrida_net}
TAVRIDA_REPO_ROOT=${TAVRIDA_REPO_ROOT}

GHCR_REGISTRY=${GHCR_REGISTRY:-ghcr.io}
GHCR_OWNER=${GHCR_OWNER}
GIT_SHA=${GIT_SHA}

POSTGRES_USER=${POSTGRES_USER:-postgres}
POSTGRES_DB=${POSTGRES_DB:-tavrida_lot}
RABBITMQ_USER=${RABBITMQ_USER:-guest}
MINIO_ROOT_USER=${MINIO_ROOT_USER:-minioadmin}

LOGTO_STORAGE_BUCKET=${LOGTO_STORAGE_BUCKET:-logto-avatars}
LOGTO_STORAGE_INTERNAL_ENDPOINT=${LOGTO_STORAGE_INTERNAL_ENDPOINT:-http://minio:9000}
LOGTO_STORAGE_PUBLIC_URL=${LOGTO_STORAGE_PUBLIC_URL:-https://s3.${DEV_DOMAIN}/logto-avatars}
LOGTO_SWARM_SERVICE=${LOGTO_SWARM_SERVICE:-tavrida-dev_logto}

LOGTO_ENDPOINT=${LOGTO_ENDPOINT:-https://auth.${DEV_DOMAIN}}
LOGTO_JWKS_URL=${LOGTO_JWKS_URL:-https://auth.${DEV_DOMAIN}/oidc/jwks}
LOGTO_AUDIENCE=${LOGTO_AUDIENCE:-https://api.${DEV_DOMAIN}}
LOGTO_M2M_APP_ID=${LOGTO_M2M_APP_ID:-}
LOGTO_M2M_RESOURCE=${LOGTO_M2M_RESOURCE:-https://default.logto.app/api}
FRONTEND_ORIGIN=${FRONTEND_ORIGIN:-https://app.${DEV_DOMAIN}}

VITE_LOGTO_ENDPOINT=${VITE_LOGTO_ENDPOINT:-https://auth.${DEV_DOMAIN}}
VITE_LOGTO_APP_ID=${VITE_LOGTO_APP_ID:-}
VITE_LOGTO_API_RESOURCE=${VITE_LOGTO_API_RESOURCE:-https://api.${DEV_DOMAIN}}

KETO_READ_URL=${KETO_READ_URL:-http://keto:4466}
KETO_WRITE_URL=${KETO_WRITE_URL:-http://keto:4467}
EOF

echo "Wrote ${OUT} (GIT_SHA=${GIT_SHA})" >&2
